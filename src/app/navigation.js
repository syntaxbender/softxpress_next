const flattenObj = (ob) => {
  let res = [];

  for(const i in ob){
    const {sub, ...rest} = ob[i]
    res.push({...rest});
    if(sub && Object.values(sub).length>0){
      const flatted = flattenObj(sub);
      res = [...res,...flatted]
    }
  }
  return res;
  
};
